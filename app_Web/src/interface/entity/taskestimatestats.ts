/**
 * 任务工时统计
 *
 * @export
 * @interface taskestimatestats
 */
export interface taskestimatestats {

    /**
     * 用户
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    account?: any;

    /**
     * 日期
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    date?: any;

    /**
     * 任务数
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    taskcnt?: any;

    /**
     * 消耗的工时
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    consumed?: any;

    /**
     * 编号
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    id?: any;

    /**
     * 项目名称
     *
     * @returns {*}
     * @memberof taskestimatestats
     */
    name?: any;
}