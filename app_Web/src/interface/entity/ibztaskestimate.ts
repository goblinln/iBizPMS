/**
 * 任务预计
 *
 * @export
 * @interface IBZTASKESTIMATE
 */
export interface IBZTASKESTIMATE {

    /**
     * 用户
     *
     * @returns {*}
     * @memberof IBZTASKESTIMATE
     */
    account?: any;

    /**
     * 预计剩余
     *
     * @returns {*}
     * @memberof IBZTASKESTIMATE
     */
    left?: any;

    /**
     * 总计消耗
     *
     * @returns {*}
     * @memberof IBZTASKESTIMATE
     */
    consumed?: any;

    /**
     * 编号
     *
     * @returns {*}
     * @memberof IBZTASKESTIMATE
     */
    id?: any;

    /**
     * 日期
     *
     * @returns {*}
     * @memberof IBZTASKESTIMATE
     */
    date?: any;

    /**
     * work
     *
     * @returns {*}
     * @memberof IBZTASKESTIMATE
     */
    work?: any;

    /**
     * 任务
     *
     * @returns {*}
     * @memberof IBZTASKESTIMATE
     */
    task?: any;
}