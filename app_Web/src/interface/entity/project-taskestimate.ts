/**
 * 项目工时统计
 *
 * @export
 * @interface ProjectTaskestimate
 */
export interface ProjectTaskestimate {

    /**
     * 项目名称
     *
     * @returns {*}
     * @memberof ProjectTaskestimate
     */
    projectname?: any;

    /**
     * 评估工时
     *
     * @returns {*}
     * @memberof ProjectTaskestimate
     */
    evaluationtime?: any;

    /**
     * 月
     *
     * @returns {*}
     * @memberof ProjectTaskestimate
     */
    month?: any;

    /**
     * 日期
     *
     * @returns {*}
     * @memberof ProjectTaskestimate
     */
    date?: any;

    /**
     * 评估成本
     *
     * @returns {*}
     * @memberof ProjectTaskestimate
     */
    evaluationcost?: any;

    /**
     * 投入成本
     *
     * @returns {*}
     * @memberof ProjectTaskestimate
     */
    inputcost?: any;

    /**
     * 工时
     *
     * @returns {*}
     * @memberof ProjectTaskestimate
     */
    consumed?: any;

    /**
     * 主键
     *
     * @returns {*}
     * @memberof ProjectTaskestimate
     */
    id?: any;

    /**
     * 项目标识
     *
     * @returns {*}
     * @memberof ProjectTaskestimate
     */
    project?: any;

    /**
     * 年
     *
     * @returns {*}
     * @memberof ProjectTaskestimate
     */
    year?: any;

    /**
     * 用户
     *
     * @returns {*}
     * @memberof ProjectTaskestimate
     */
    account?: any;

    /**
     * 其他项目工时
     *
     * @returns {*}
     * @memberof ProjectTaskestimate
     */
    otherconsumed?: any;

    /**
     * 其他项目评估工时
     *
     * @returns {*}
     * @memberof ProjectTaskestimate
     */
    otherevaluationtime?: any;
}