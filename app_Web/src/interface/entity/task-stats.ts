/**
 * 任务统计
 *
 * @export
 * @interface TaskStats
 */
export interface TaskStats {

    /**
     * 项目名称
     *
     * @returns {*}
     * @memberof TaskStats
     */
    projectname?: any;

    /**
     * 预计总工时
     *
     * @returns {*}
     * @memberof TaskStats
     */
    totalestimate?: any;

    /**
     * 消耗总工时
     *
     * @returns {*}
     * @memberof TaskStats
     */
    totalconsumed?: any;

    /**
     * 剩余总工时
     *
     * @returns {*}
     * @memberof TaskStats
     */
    totalleft?: any;

    /**
     * 项目
     *
     * @returns {*}
     * @memberof TaskStats
     */
    project?: any;

    /**
     * 编号
     *
     * @returns {*}
     * @memberof TaskStats
     */
    id?: any;

    /**
     * 名称
     *
     * @returns {*}
     * @memberof TaskStats
     */
    name?: any;

    /**
     * 完成者
     *
     * @returns {*}
     * @memberof TaskStats
     */
    finishedby?: any;

    /**
     * 效率
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskefficient?: any;

    /**
     * 任务编号
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskid?: any;

    /**
     * 任务名称
     *
     * @returns {*}
     * @memberof TaskStats
     */
    taskname?: any;
}