    /**
     * 实体服务对象
     *
     * @type {SubProductPlanService}
     * @memberof ProductPlanSubPlanCreateViewBase
     */
    public appEntityService: SubProductPlanService = new SubProductPlanService;


    /**
     * 计数器服务对象集合
     *
     * @type {Array<*>}
     * @memberof ProductPlanSubPlanCreateViewBase
     */    
